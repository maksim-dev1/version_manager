import 'dart:convert';
import 'dart:math';
import 'package:crypto/crypto.dart';

/// Сервис для генерации и хеширования токенов аутентификации.
///
/// Предоставляет методы для создания криптографически стойких токенов доступа и их
/// безопасного хеширования для хранения в базе данных. Поддерживает паттерн
/// "refresh token rotation" для повышения безопасности.
///
/// ## Типы токенов
/// - **Access Token** — краткосрочный токен (обычно 1 час) для аутентификации API запросов
/// - **Refresh Token** — долгосрочный токен (обычно 30 дней) только для получения новых токенов
///
/// ## Безопасность
/// - Использует `Random.secure()` для криптографической стойкости
/// - Токены имеют длину 64 байта (512 бит энтропии)
/// - Хеширование через SHA-256 для безопасного хранения
/// - Поддерживает Base64URL кодирование без padding символов
///
/// ### Пример использования
/// ```dart
/// final service = TokenService();
///
/// // Генерация пары токенов
/// final pair = service.generateTokenPair();
///
/// // Хеширование для БД
/// final hash = service.hashToken(pair.accessToken);
/// ```
class TokenService {
  /// Длина токена в байтах (512 бит энтропии).
  static const int _tokenLength = 64;

  /// Генерирует криптографически стойкий случайный токен.
  ///
  /// Создаёт токен высокой энтропии с использованием `Random.secure()` для
  /// максимальной защиты от атак предсказания. Токен кодируется в Base64URL
  /// без padding символов для URL-безопасности.
  ///
  /// ### Характеристики
  /// - Длина: 64 байта (512 бит энтропии)
  /// - Кодирование: Base64URL без '=' символов
  /// - Источник случайности: `Random.secure()`
  /// - Вероятность коллизии: практически невозможна
  ///
  /// ### Возвращает
  /// URL-безопасную строку токена длиной примерно 85-86 символов.
  ///
  /// ### Пример
  /// ```dart
  /// final token = service.generateToken();
  /// // Результат: "abc123XYZ...-_" (без '=' символов)
  /// ```
  String generateToken() {
    final random = Random.secure();
    final bytes = List<int>.generate(_tokenLength, (_) => random.nextInt(256));
    return base64Url.encode(bytes).replaceAll('=', '');
  }

  /// Хеширует токен для безопасного хранения в базе данных.
  ///
  /// Преобразует исходный токен в необратимый SHA-256 хеш для защиты от утечек.
  /// Даже если база данных скомпрометирована, исходные токены восстановить невозможно.
  /// Это соответствует принципам безопасности "defense in depth".
  ///
  /// ### Параметры
  /// - [token] — исходный токен для хеширования
  ///
  /// ### Возвращает
  /// Hex-строку SHA-256 хеша длиной 64 символа.
  ///
  /// ### Применение
  /// - Хранение в поле `tokenHash` таблицы [AuthSession]
  /// - Поиск сессии по хешу токена при аутентификации
  /// - Инвалидация токенов через удаление записей с хешем
  ///
  /// ### Пример
  /// ```dart
  /// final hash = service.hashToken(accessToken);
  /// // Результат: "a1b2c3d4e5f6..." (64 символа)
  ///
  /// // Сохранение в БД
  /// await AuthSession.db.insertRow(session, AuthSession(
  ///   tokenHash: hash,
  ///   // ...
  /// ));
  /// ```
  String hashToken(String token) {
    final bytes = utf8.encode(token);
    final digest = sha256.convert(bytes);
    return digest.toString();
  }

  /// Генерирует пару токенов для реализации паттерна "Refresh Token Rotation".
  ///
  /// Создаёт два независимых токена с разным предназначением:
  /// - **Access Token** — для краткосрочной аутентификации API (обычно 1 час)
  /// - **Refresh Token** — для долгосрочного обновления токенов (обычно 30 дней)
  ///
  /// ### Возвращает
  /// [TokenPair] с двумя независимо сгенерированными токенами.
  ///
  /// ### Паттерн использования
  /// 1. При входе — генерируется новая пара
  /// 2. При обновлении — старые токены заменяются новой парой (rotation)
  /// 3. Старые токены становятся недействительными
  ///
  /// ### Преимущества ротации
  /// - Ограниченное время жизни скомпрометированных токенов
  /// - Обнаружение повторного использования токенов
  /// - Автоматическая инвалидация при подозрительной активности
  ///
  /// ### Пример
  /// ```dart
  /// // При входе/регистрации
  /// final tokens = service.generateTokenPair();
  ///
  /// // Сохранение в сессии
  /// final authSession = AuthSession(
  ///   tokenHash: service.hashToken(tokens.accessToken),
  ///   refreshTokenHash: service.hashToken(tokens.refreshToken),
  ///   expiresAt: DateTime.now().add(Duration(hours: 1)),
  ///   refreshExpiresAt: DateTime.now().add(Duration(days: 30)),
  /// );
  /// ```
  TokenPair generateTokenPair() {
    return TokenPair(
      accessToken: generateToken(),
      refreshToken: generateToken(),
    );
  }
}

/// Пара токенов для аутентификации с разделением ответственности.
///
/// Представляет два токена с разным предназначением и временем жизни:
/// - [accessToken] — для краткосрочной аутентификации API запросов
/// - [refreshToken] — для долгосрочного обновления пары токенов
///
/// Этот подход повышает безопасность, ограничивая время жизни токенов доступа
/// и обеспечивая возможность автоматического обновления без повторного входа.
///
/// ### Жизненный цикл
/// 1. **Создание** — при входе/регистрации пользователя
/// 2. **Использование** — access token в заголовках API запросов
/// 3. **Обновление** — refresh token для получения новой пары
/// 4. **Инвалидация** — при выходе или подозрительной активности
class TokenPair {
  /// Токен доступа для аутентификации API запросов.
  ///
  /// **Время жизни:** обычно 1 час
  /// **Использование:** передаётся в заголовке `Authorization: Bearer <token>`
  /// **Назначение:** подтверждение личности при каждом API вызове
  final String accessToken;

  /// Токен обновления для получения новой пары токенов.
  ///
  /// **Время жизни:** обычно 30 дней
  /// **Использование:** только в эндпоинте `/auth/refresh-tokens`
  /// **Назначение:** обновление пары без повторного ввода пароля
  /// **Безопасность:** используется только один раз (token rotation)
  final String refreshToken;

  /// Создаёт пару токенов.
  ///
  /// ### Параметры
  /// - [accessToken] — токен доступа для API
  /// - [refreshToken] — токен для обновления пары
  TokenPair({
    required this.accessToken,
    required this.refreshToken,
  });
}
